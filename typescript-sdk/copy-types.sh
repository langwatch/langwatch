#!/bin/bash

set -e

cp ../langwatch/src/server/tracer/types.ts src/internal/generated/types/tracer.ts
ts-to-zod src/internal/generated/types/tracer.ts src/internal/generated/types/tracer.generated.ts

# Copy filter types (only filterFieldsEnum is needed by the SDK)
mkdir -p src/internal/generated/filters
node -e "
const fs = require('fs');
const src = fs.readFileSync('../langwatch/src/server/filters/types.ts', 'utf8');
// Extract only the zod import and filterFieldsEnum definition
const lines = src.split('\n');
const out = [];
let inEnum = false;
for (const line of lines) {
  if (line.match(/^import.*from ['\"]zod['\"]/)) { out.push(line); continue; }
  if (line.match(/export const filterFieldsEnum/)) { inEnum = true; }
  if (inEnum) { out.push(line); }
  if (inEnum && line.includes(']);')) { inEnum = false; }
  if (line.match(/export type FilterField/)) { out.push(line); }
}
fs.writeFileSync('src/internal/generated/filters/types.ts', out.join('\n') + '\n');
"

cp ../langwatch/src/server/evaluations/types.ts src/internal/generated/types/evaluations.ts
if [[ "$OSTYPE" == "darwin"* ]]; then
  sed -i '' 's/\.\.\/tracer\/types\.generated/.\/tracer.generated/g' src/internal/generated/types/evaluations.ts
else
  sed -i 's/\.\.\/tracer\/types\.generated/.\/tracer.generated/g' src/internal/generated/types/evaluations.ts
fi
# evaluations.ts is Zod-first (schemas defined with z.*), so ts-to-zod
# cannot generate from it. Write an empty barrel instead.
cat > src/internal/generated/types/evaluations.generated.ts << 'GENERATED'
// Generated by copy-types.sh
// evaluations.ts is Zod-first — all schemas are exported directly from it.
export {};
GENERATED

cp ../langevals/ts-integration/evaluators.generated.ts src/internal/generated/types/evaluators.generated.ts
ts-to-zod src/internal/generated/types/evaluators.generated.ts src/internal/generated/types/evaluators.zod.generated.ts

# Fix z.record(z.never()) to z.record(z.string(), z.never())
if [[ "$OSTYPE" == "darwin"* ]]; then
  sed -i '' 's/z\.record(z\.never())/z.record(z.string(), z.never())/g' src/internal/generated/types/evaluators.zod.generated.ts
  sed -i '' 's/z\.record(z\.any())/z.record(z.string(), z.any())/g' src/internal/generated/types/tracer.generated.ts
else
  sed -i 's/z\.record(z\.never())/z.record(z.string(), z.never())/g' src/internal/generated/types/evaluators.zod.generated.ts
  sed -i 's/z\.record(z\.any())/z.record(z.string(), z.any())/g' src/internal/generated/types/tracer.generated.ts
fi
