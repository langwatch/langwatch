/**
 * Developer-mode card gallery — every card Langy can produce, on demand.
 *
 * Most of Langy's UI only appears in response to a live agent turn: you cannot
 * see the "deleting a dashboard" card without deleting a dashboard, or the
 * agent-at-capacity error without the agent being at capacity. That made the
 * card kit effectively untestable by eye — you shipped a card, then waited for
 * the universe to produce the conditions that render it.
 *
 * This renders the whole kit from FIXTURES, in the panel, in place of the
 * conversation. Open it from ⋯ → Card gallery (developer mode only).
 *
 * The cards here are the REAL components fed real-shaped data — not mock-ups of
 * them. Every capability card goes through `LangyCapabilityRenderer` off a
 * genuine `langwatch.<resource>.<verb>` tool name, so the gallery also exercises
 * the registry: if a rename breaks the tool-name → card resolution, the card
 * silently disappears from this list, and that IS the bug.
 *
 * The error cards are generated by iterating `KNOWN_LANGY_ERROR_KINDS` through
 * `explainLangyError`, so a new backend error kind shows up here automatically
 * with whatever copy it was given (and a `suppress` kind correctly renders as
 * its connect card instead of an error — which is the behaviour item 19 hangs
 * on, made visible).
 */
