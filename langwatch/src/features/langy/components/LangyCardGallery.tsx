/** A settled tool call, shaped exactly as the stream delivers one. */
function call(name: string, output: unknown, input: unknown = {}) {
  return { name, state: "output-available", input, output };
}

/** Fixed so the gallery's trace times don't drift between renders. */
const TRACE_FIXTURE_NOW = Date.parse("2026-07-11T14:03:00Z");

/**
 * One trace, in the shape `POST /api/trace/search` actually returns under
 * `--format json` (`traceSchema` in `server/tracer/types.ts`): snake_case ids,
 * `input`/`output` as `{ value }` envelopes, `timestamps.started_at` in epoch
 * ms, metrics nested. Fixtures that drift from the real shape are worse than no
 * fixtures — the card would render here and break in the panel.
 */
function traceFixture({
  id,
  startedAt,
  input,
  output,
  totalTimeMs,
  totalCost,
  error,
}: {
  id: string;
  startedAt: number;
  input: string;
  output?: string;
  totalTimeMs: number;
  totalCost: number;
  error?: { message: string };
}) {
  return {
    trace_id: id,
    project_id: "project_gallery",
    timestamps: { started_at: startedAt, inserted_at: startedAt },
    input: { value: input },
    ...(output ? { output: { value: output } } : {}),
    metrics: { total_time_ms: totalTimeMs, total_cost: totalCost },
    ...(error ? { error } : {}),
  };
}

/** The tool-call parts of an assistant message, as AI-SDK emits them. */
function toolMessage(
  parts: Array<{
    name: string;
    state: string;
    input?: unknown;
    output?: unknown;
  }>,
): UIMessage {
  return {
    id: "gallery",
    role: "assistant",
    parts: parts.map((part, index) => ({
      type: `tool-${part.name}`,
      toolCallId: `gallery-${index}`,
      state: part.state,
      input: part.input,
      output: part.output,
    })),
  } as unknown as UIMessage;
}

