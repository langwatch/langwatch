-- Claude Code enhanced-telemetry per-project gate (plan §4.1 / C0).
--
-- The claudeCodeEnhancedTelemetryGate reactor sets this true (once) the first
-- time a project sends real Claude Code tracing spans (scope
-- `com.anthropic.claude_code.tracing`). While true, log ingest stops stamping
-- `langwatch.claude_code.kind` on content logs, so the synthesis fold produces
-- no spans (killing the double-count) and content logs revert to normal project
-- retention. Write-once, never reset.
--
-- Nullable-free but defaulted, so the add is safe on the live table with no
-- backfill: existing rows read false (pre-gate behavior preserved).
ALTER TABLE "Project" ADD COLUMN IF NOT EXISTS "claudeCodeEnhancedTelemetry" BOOLEAN NOT NULL DEFAULT false;
