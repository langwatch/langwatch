package herrgen

import (
	"fmt"
	"regexp"
	"slices"
	"strings"
)

// docWidth is the column the doc comments wrap at, counted from the start of
// the text: three spaces of indent plus "* " puts the longest line at 79.
const docWidth = 74

const header = `// Code generated by cmd/herrgen. DO NOT EDIT.
// Source: the ` + "`herr.Code`" + ` declarations in the Go services.
// Regenerate with ` + "`make herrgen`" + ` (or ` + "`go run ./cmd/herrgen`" + `).

/**
 * Error codes declared by the Go services, generated so the TypeScript control
 * plane fails to compile when a Go service gains a code that has no
 * customer-facing presentation.
 *
 * The doc text on each entry is the ENGINEER-facing Go doc comment. It is NOT
 * customer copy and must never be rendered to a user — it exists to inform
 * whoever writes the presentation entry.
 */
export const goErrorCodes = {
`

const footer = `} as const;

export type GoErrorCode = keyof typeof goErrorCodes;
`

// nodeHeader opens the nodeErrorCodes block. Its leading blank line separates it
// from the goErrorCodes footer it is appended after in the same file.
const nodeHeader = `
/**
 * Workflow NodeError codes produced by the nlpgo engine (the ` + "`Type`" + ` field of
 * ` + "`NodeError`" + `). Generated for the same reason as ` + "`goErrorCodes`" + `: the client
 * presentation registry is exhaustive over them, so a new node-error type in
 * Go fails the TypeScript build until its customer copy is written.
 */
export const nodeErrorCodes = {
`

const nodeFooter = `} as const;

export type NodeErrorCode = keyof typeof nodeErrorCodes;
`

// Render writes the TypeScript for the parsed entries.
func Render(entries []Entry) []byte {
	var out strings.Builder
	out.WriteString(header)
	for _, entry := range entries {
		out.WriteString("  /**\n")
		for _, line := range docLines(entry) {
			if line == "" {
				out.WriteString("   *\n")
				continue
			}
			fmt.Fprintf(&out, "   * %s\n", line)
		}
		out.WriteString("   */\n")
		fmt.Fprintf(&out, "  %s: { service: %q", propertyKey(entry.Code), entry.Primary().Service)
		if entry.HasStatus {
			fmt.Fprintf(&out, ", httpStatus: %d", entry.Status)
		}
		out.WriteString(" },\n")
	}
	out.WriteString(footer)
	return []byte(out.String())
}

// RenderNodeCodes writes the nodeErrorCodes block that follows goErrorCodes in
// the same generated file. There are no Go doc comments on a NodeError literal,
// so each entry carries only the @source files it was found in — sorted, so the
// bytes are stable across runs.
func RenderNodeCodes(codes []NodeCode) []byte {
	var out strings.Builder
	out.WriteString(nodeHeader)
	for _, code := range codes {
		out.WriteString("  /**\n")
		for _, source := range code.Sources {
			fmt.Fprintf(&out, "   * @source %s\n", source)
		}
		out.WriteString("   */\n")
		fmt.Fprintf(&out, "  %s: { service: %q },\n", propertyKey(code.Code), serviceOf(code.Sources[0]))
	}
	out.WriteString(nodeFooter)
	return []byte(out.String())
}

var paragraphBreak = regexp.MustCompile(`\n[ \t]*\n`)

// docLines renders one entry's doc block, without the leading "* ".
func docLines(entry Entry) []string {
	primary := entry.Primary()

	// The Go convention starts a doc comment with the identifier it documents;
	// the em dash keeps that lead-in readable once it is a TypeScript comment.
	body := strings.TrimSpace(primary.Doc)
	if rest, found := strings.CutPrefix(body, primary.Name); found {
		body = strings.TrimSpace(rest)
	}
	lead := primary.Name
	if body != "" {
		lead += " — " + body
	}

	paragraphs := paragraphBreak.Split(lead, -1)
	if others := alsoDeclaredBy(entry); others != "" {
		paragraphs = append(paragraphs, others)
	}

	var lines []string
	for _, paragraph := range paragraphs {
		if len(lines) > 0 {
			lines = append(lines, "")
		}
		lines = append(lines, wrap(escapeComment(paragraph), docWidth)...)
	}

	lines = append(lines, "")
	for _, source := range sources(entry) {
		lines = append(lines, "@source "+source)
	}
	return lines
}

// alsoDeclaredBy names the other services declaring the same code string, so a
// reader of the generated file knows the entry is shared before changing it.
func alsoDeclaredBy(entry Entry) string {
	if len(entry.Declarations) < 2 {
		return ""
	}
	others := make([]string, 0, len(entry.Declarations)-1)
	for _, declaration := range entry.Declarations[1:] {
		others = append(others, fmt.Sprintf("%s (%s)", declaration.Service, declaration.Name))
	}
	slices.Sort(others)
	return "Also declared by " + strings.Join(others, ", ") + "."
}

// sources lists every file declaring the code, in path order.
func sources(entry Entry) []string {
	paths := make([]string, 0, len(entry.Declarations))
	for _, declaration := range entry.Declarations {
		paths = append(paths, declaration.Source)
	}
	slices.Sort(paths)
	return slices.Compact(paths)
}

// escapeComment neutralizes a block-comment terminator that a Go doc comment
// happened to contain, which would otherwise end the generated comment early.
func escapeComment(text string) string {
	return strings.ReplaceAll(text, "*/", "*\\/")
}

// wrap re-flows a paragraph to width, collapsing the Go comment's own line
// breaks so the TypeScript comment wraps on its own terms.
func wrap(paragraph string, width int) []string {
	words := strings.Fields(paragraph)
	if len(words) == 0 {
		return nil
	}
	lines := []string{words[0]}
	for _, word := range words[1:] {
		last := len(lines) - 1
		if len([]rune(lines[last]))+1+len([]rune(word)) <= width {
			lines[last] += " " + word
			continue
		}
		lines = append(lines, word)
	}
	return lines
}

var identifier = regexp.MustCompile(`^[A-Za-z_$][A-Za-z0-9_$]*$`)

// propertyKey quotes a code that is not a bare JavaScript identifier.
func propertyKey(code string) string {
	if identifier.MatchString(code) {
		return code
	}
	return fmt.Sprintf("%q", code)
}
